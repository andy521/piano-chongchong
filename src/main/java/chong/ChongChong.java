package chong;

/**
 */
public class ChongChong {
	private String name;
	private String url;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public ChongChong() {
		super();
	}

	public ChongChong(String name, String url) {
		super();
		this.name = name;
		this.url = url;
	}
}
